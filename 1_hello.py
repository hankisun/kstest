
ch={
    "name":"기사",
    "level":12,
    "items":{
        "sword":"검",
        "armor":"갑옷"
    },
    "skill":["베기","세게 베기","아주 세게 베기"]
}

for key in ch:
    if type(ch[key]) is str:
        print(key,":",ch[key])
    elif type(ch[key]) is list:
        for i in ch[key]:
            print(key,":",i)
    elif type(ch[key]) is dict:
        for j in ch[key]:
            print(j,":",ch[key][j])
    elif type(ch[key]) is int:
        print(key,":",ch[key])
    else:
        print()

print(ch["items"])     
print(ch["skill"])   

